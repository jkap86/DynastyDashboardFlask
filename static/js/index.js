function toggle() {
	var x = document.getElementById("view-leagues")
	var y = document.getElementById("view-common")
	var z = document.getElementById("view-transactions")
	var a = document.getElementById("view-all-leaguemates")
	var b = document.getElementById("view-player-search")
	var xx = document.getElementById("nav-view-leagues")
	var yy = document.getElementById("nav-view-common")
	var zz = document.getElementById("nav-view-transactions")
	var aa = document.getElementById("nav-view-all-leaguemates")
	var bb = document.getElementById("nav-search-player")

	if (x.style.display == "none") {
		x.style.display = "inline";
		y.style.display = "none";
		z.style.display = "none";
		a.style.display = "none";
		b.style.display = "none";
		xx.style.backgroundColor = "red";
		yy.style.backgroundColor = "#008B8B";
		zz.style.backgroundColor = "#008B8B";
		aa.style.backgroundColor = "#008B8B";
		bb.style.backgroundColor = "#008B8B";
	}
}

